//Arithmetic Assignment operators in Java

public class App {
    public static void main(String args[]) {
        int a = 123;

        System.out.println(a);
        a += 10;
        System.out.println(a);
        a -= 10;// a=a-10
        System.out.println(a);
        a *= 10;
        System.out.println(a);
        a /= 10;
        System.out.println(a);
        a %= 10;
        System.out.println(a);
    }
}
