//Unary Operators in Java

public class App {
    public static void main(String[] args) {
        // Unary Operators in Java ++ --
        int a = 10;
        System.out.println(a);
        // a++; //a=a+1
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
    }
}
